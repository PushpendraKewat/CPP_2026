#include<iostream>
#include<vector>
#include<climits>
#include<algorithm>

using std::cout;
using std::cin;
using std::endl;
using std::vector;

class Solution {
public:
    vector<int> majorityElement(vector<int>& nums) {
        int count1 = 0, count2 = 0;
        int candidate1 = 0, candidate2 = 1; // different initial values

        // Step 1: Find potential candidates
        for(int num : nums){
            if(num == candidate1){
                count1++;
            }
            else if(num == candidate2){
                count2++;
            }
            else if(count1 == 0){
                candidate1 = num;
                count1 = 1;
            }
            else if(count2 == 0){
                candidate2 = num;
                count2 = 1;
            }
            else{
                count1--;
                count2--;
            }
        }

        // Step 2: Verify candidates
        count1 = count2 = 0;
        for(int num : nums){
            if(num == candidate1) count1++;
            else if(num == candidate2) count2++;
        }

        vector<int> result;
        if(count1 > nums.size()/3) result.push_back(candidate1);
        if(count2 > nums.size()/3) result.push_back(candidate2);

        return result;
    }
};